CREATE TABLE IF NOT EXISTS PRODUCT (
    ID VARBINARY(2147483647) PRIMARY KEY,
    NAME VARCHAR(35)NOT NULL,
    BRAND VARCHAR(35),
    BARCODE VARCHAR(25),
    PRODUCT_CATEGORY_ID VARBINARY(2147483647),
    QUANTITY_ID VARBINARY(2147483647));

